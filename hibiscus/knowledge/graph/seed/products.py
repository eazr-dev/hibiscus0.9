# ── Cypher ─────────────────────────────────────────────────────────────────────

_MERGE_PRODUCT = """
MERGE (p:Product {name: $name})
SET
  p.insurer_name        = $insurer_name,
  p.category            = $category,
  p.type                = $type,
  p.sum_insured_min     = $sum_insured_min,
  p.sum_insured_max     = $sum_insured_max,
  p.premium_range_min   = $premium_range_min,
  p.premium_range_max   = $premium_range_max,
  p.key_features        = $key_features,
  p.exclusion_count     = $exclusion_count,
  p.sub_limit_count     = $sub_limit_count,
  p.waiting_periods     = $waiting_periods,
  p.copay_structure     = $copay_structure,
  p.room_rent_limit     = $room_rent_limit,
  p.eazr_score          = $eazr_score,
  p.updated_at          = datetime()
RETURN p.name AS name
"""

_MERGE_OFFERS_REL = """
MATCH (i:Insurer {name: $insurer_name})
MATCH (p:Product {name: $product_name})
MERGE (i)-[:OFFERS]->(p)
"""


async def seed_products(client: Neo4jClient) -> None:
    """
    MERGE all Product nodes and OFFERS relationships into Neo4j.
    Idempotent — safe to re-run.
    """
    logger.info("seed_products_start", count=len(PRODUCTS))

    # Prepare serialisable params (convert nested dicts/lists to strings where needed)
    product_params = []
    for p in PRODUCTS:
        params = dict(p)
        # Neo4j can store lists of strings natively; dicts need to be stringified
        params["waiting_periods"] = str(p.get("waiting_periods", {}))
        # Remove non-schema keys that we don't want in Product nodes
        for extra_key in [
            "restore_benefit", "multiplier_benefit", "wellness_benefits", "unlimited_restore",
            "lock_the_clock", "global_cover", "health_returns", "wellness_integration",
            "vitality_program", "zero_copay", "digital_claims", "zero_dep", "paperless",
            "digital", "cashless_garages", "cashless_approval_mins", "claim_approval_hrs",
            "better_half_benefit", "joint_life", "return_of_premium", "whole_life_option",
            "special_exit_value", "ci_conditions_covered", "unlimited_si", "fund_options",
            "lock_in_years", "annual_premium_above_2_5L_taxable_gains", "plan_options",
            "irr_approx", "note", "target_segment", "min_entry_age", "max_entry_age",
            "age_limit", "government_backed", "mandatory", "guaranteed_returns",
            "digital_score", "chronic_management",
        ]:
            params.pop(extra_key, None)
        product_params.append(params)

    succeeded = await client.execute_batch(
        _MERGE_PRODUCT,
        param_list=product_params,
        query_name="seed_products",
    )
    logger.info("seed_products_nodes_complete", succeeded=succeeded, total=len(PRODUCTS))

    # Create OFFERS relationships
    rel_params = [
        {"insurer_name": p["insurer_name"], "product_name": p["name"]}
        for p in PRODUCTS
    ]
    rel_ok = await client.execute_batch(
        _MERGE_OFFERS_REL,
        param_list=rel_params,
        query_name="seed_offers_relationships",
    )
    logger.info("seed_offers_relationships_complete", succeeded=rel_ok, total=len(rel_params))
