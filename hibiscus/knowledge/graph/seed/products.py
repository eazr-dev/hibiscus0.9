# ── Helper ─────────────────────────────────────────────────────────────────────

def L(lakhs: float) -> int:
    """Convert lakhs to INR."""
    return int(lakhs * 100_000)


def Cr(crores: float) -> int:
    """Convert crores to INR."""
    return int(crores * 10_000_000)


